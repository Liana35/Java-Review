package Review;

public class LoopsReview {
    public static void main(String[] args) {
       //while loop
        // when we dont know how many times to repeat the code - while or do while
        int a= 1;
        while(a<=3){
            System.out.println("I am a while loop");
            a++;
        }

        // do while
        // executes the code at least once

        int b=1;
        do{
            System.out.println("do while");

        }while(b>=3);

}
}